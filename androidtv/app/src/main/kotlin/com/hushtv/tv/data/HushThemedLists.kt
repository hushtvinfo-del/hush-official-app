package com.hushtv.tv.data

import androidx.compose.ui.graphics.Color

/**
 * One pre-curated movie reference for a [ThemedList].
 *
 *  • [title]   — primary title used for [LibraryIndex.findBest].
 *                Use the canonical English title; the matcher
 *                normalises case + punctuation already.
 *  • [year]    — release year hint. The matcher accepts ±1 year
 *                slop. Highly recommended for any title that has
 *                multiple versions or remakes (e.g. "Dune" 1984
 *                vs "Dune" 2021), otherwise omit.
 *  • [altTitles] — fallback titles tried in order if [title]
 *                misses. Useful for international releases ("La
 *                Vita è Bella" → "Life Is Beautiful") or known
 *                Xtream re-titlings.
 */
data class ThemedMovieRef(
    val title: String,
    val year: Int? = null,
    val altTitles: List<String> = emptyList(),
)

/**
 * One library hit for a [ThemedMovieRef]. Carries everything the
 * UI needs to render a poster card and deep-link into the standard
 * `moviedetail/{playlistId}/{streamId}/{title}` route.
 *
 * Note: [poster] is the poster Xtream returned for this title.
 * [year] is the year parsed from the library title, NOT the year
 * the curator typed — the curator's year is only used as a match
 * tiebreaker.
 */
data class ThemedLibraryMatch(
    val ref: ThemedMovieRef,
    val streamId: Int,
    val title: String,
    val poster: String?,
    val year: Int?,
)

/**
 * One themed movie list that appears on the "Moods & Themes" home
 * page. Every entry in [HushThemedLists.all] becomes a catalog tile;
 * tapping opens the themed detail screen.
 *
 * Performance shape (v1.43.13)
 * ────────────────────────────
 * Pre-curated titles only — no TMDB discover API at all. Each
 * [ThemedMovieRef] is matched against the user's library
 * synchronously via [LibraryIndex.findBest], which is in-memory
 * and runs in well under 1 ms per title. So a 25-title theme
 * resolves its full poster grid in 25 ms, not 4-8 seconds. No
 * network on the rendering hot path; no "Curating…" splash;
 * theme tiles paint with real movie art on first frame because
 * the first matched library movie's poster is the tile backdrop.
 *
 * The trade-off: someone has to maintain the curated lists. That's
 * a feature, not a bug — the user said "tons of movies for each
 * theme, top 100 if you can find them" and pre-curation lets us
 * pick the actual right films instead of relying on TMDB's noisy
 * keyword index that returned wrong results in v1.43.11/12.
 */
data class ThemedList(
    val id: String,
    val title: String,
    val subtitle: String,
    val section: Section,
    val accent: Color,
    val glyph: String,
    /** Pre-curated film list. Order is preserved on display. */
    val movies: List<ThemedMovieRef>,
    /**
     * Hand-picked, full-resolution TMDB CDN URL used as the
     * theme's hero backdrop on the home preview pill, the catalog
     * grid hero, and the detail screen title backdrop. Resolved at
     * compile time from [HushThemedLists.HERO_BACKDROPS] — no
     * network, no race condition, paints on first frame every time.
     *
     * Why hardcoded TMDB URLs and not the user's IPTV library
     * poster: IPTV providers ship 250-500 px posters optimised for
     * tile thumbnails. When upscaled to half-screen on a 4K TV
     * those look catastrophically blurry. TMDB `original`-size
     * backdrops are 1920×1080 stills sourced from studios — the
     * exact same artwork Netflix uses for its hero rails.
     */
    val heroBackdropUrl: String,
) {
    enum class Section(val displayName: String) {
        NARRATIVE("Narrative & Story"),
        MASHUP("Genre Mashups"),
        VIBE("Vibe & Experience"),
        BONUS("High-Engagement Picks"),
    }
}

