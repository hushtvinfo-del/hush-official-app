    /**
     * Resolve a [ThemedList] against the user's library. Pure
     * synchronous — runs in well under 1 ms per title because
     * [LibraryIndex.findBest] is in-memory.
     *
     * Behaviour:
     *   • Hits the primary [ThemedMovieRef.title] first (year-aware).
     *   • Falls back to each of [ThemedMovieRef.altTitles] in order.
     *   • De-duplicates by streamId so a single library entry never
     *     appears twice when two refs collapse onto the same film.
     *   • Preserves the curator's input order.
     *
     * Caller is responsible for ensuring [LibraryIndex] has been
     * primed (boot refresh always does this; theme detail screen
     * also primes defensively as a safety net).
     */
    fun matchAgainstLibrary(theme: ThemedList): List<ThemedLibraryMatch> {
        val out = ArrayList<ThemedLibraryMatch>(theme.movies.size)
        val seenStreamIds = HashSet<Int>()
        for (ref in theme.movies) {
            val candidates = listOf(ref.title) + ref.altTitles
            for (candidate in candidates) {
                val hit = LibraryIndex.findBest(candidate, "movie", ref.year) ?: continue
                if (!seenStreamIds.add(hit.streamId)) break
                out += ThemedLibraryMatch(
                    ref = ref,
                    streamId = hit.streamId,
                    title = hit.title,
                    poster = hit.poster,
                    year = hit.releaseYear,
                )
                break
            }
        }
        return out
    }
